	Scissor::Scissor(I32 x, I32 y, I32 w, I32 h) 
	{
		GUARD(Scissor::Scissor);

		mRect[0] = x;
		mRect[1] = y;
		mRect[2] = w;
		mRect[3] = h;

		UNGUARD;
	}

	Scissor::~Scissor() 
	{
		GUARD(Scissor::~Scissor);

		UNGUARD;
	}

	//////////////////////////////////////////////////////////////////////////

	/*
	====================
	operator
	====================
	*/
	VOID Scissor::operator() (Context*rc)
	{
		GUARD(Scissor::operator());	

		glScissor(mRect[0], mRect[1], mRect[2], mRect[3]);

		UNGUARD;
	}

